const (
	desktopAppName       = "Codextra"
	desktopAppExecutable = "codextra-desktop"
)

// installDesktopApp generates ~/Applications/Codextra.app — a clickable launcher
// that runs `codextra --desktop`, so Codex Desktop opens behind the codextra
// proxy without touching the CLI. The bundle uses the codextra logo as its icon.
func installDesktopApp() error {
	exe, err := os.Executable()
	if err != nil {
		return fmt.Errorf("find codextra executable: %w", err)
	}
	if resolved, rerr := filepath.EvalSymlinks(exe); rerr == nil {
		exe = resolved
	}

	home, err := os.UserHomeDir()
	if err != nil {
		return err
	}
	appsDir := filepath.Join(home, "Applications")
	if err := os.MkdirAll(appsDir, 0o755); err != nil {
		return fmt.Errorf("create %s: %w", appsDir, err)
	}
	appPath := filepath.Join(appsDir, desktopAppName+".app")

	if err := writeDesktopAppBundle(appPath, exe, resolveCodexBin()); err != nil {
		return err
	}
	// Bump the bundle mtime so Launch Services refreshes the icon.
	_ = exec.Command("touch", appPath).Run()

	fmt.Printf("Installed %s\n", appPath)
	fmt.Println("Open it from Finder, Launchpad, or the Dock to launch Codex Desktop behind the codextra proxy.")
	fmt.Printf("Remove it with: rm -rf %q\n", appPath)
	return nil
}

func writeDesktopAppBundle(appPath, codextraExe, codexBin string) error {
	contents := filepath.Join(appPath, "Contents")
	macOSDir := filepath.Join(contents, "MacOS")
	resDir := filepath.Join(contents, "Resources")
	for _, d := range []string{macOSDir, resDir} {
		if err := os.MkdirAll(d, 0o755); err != nil {
			return err
		}
	}

	if err := os.WriteFile(filepath.Join(contents, "Info.plist"), []byte(desktopAppInfoPlist), 0o644); err != nil {
		return fmt.Errorf("write Info.plist: %w", err)
	}

	launcher := filepath.Join(macOSDir, desktopAppExecutable)
	if err := os.WriteFile(launcher, []byte(desktopLauncherScript(codextraExe, codexBin)), 0o755); err != nil {
		return fmt.Errorf("write launcher: %w", err)
	}

	// A generic icon is acceptable if rendering fails, so this is best-effort.
	if err := writeDesktopAppIcon(filepath.Join(resDir, "codextra.icns")); err != nil {
		log.Printf("codextra: app icon unavailable, using the system default: %v", err)
	}
	return nil
}

// resolveCodexBin locates the codex binary at install time and bakes it into the
// launcher, because macOS launches apps with a minimal PATH that won't include
// Homebrew or ~/.local/bin. Returns "" if codex can't be found now; the launcher
// then falls back to PATH at run time.
func resolveCodexBin() string {
	if v := os.Getenv("CODEXTRA_CODEX_BIN"); v != "" {
		return v
	}
	if p, err := exec.LookPath("codex"); err == nil {
		return p
	}
	return ""
}

func desktopLauncherScript(codextraExe, codexBin string) string {
	var b strings.Builder
	b.WriteString("#!/bin/sh\n")
	b.WriteString("# Generated by `codextra install-app` — launches Codex Desktop behind the codextra proxy.\n")
	b.WriteString(`export PATH="/opt/homebrew/bin:/usr/local/bin:$HOME/.local/bin:$HOME/bin:$PATH"` + "\n")
	if codexBin != "" {
		b.WriteString("export CODEXTRA_CODEX_BIN=" + shellSingleQuote(codexBin) + "\n")
	}
	b.WriteString("exec " + shellSingleQuote(codextraExe) + " --desktop\n")
	return b.String()
}

func shellSingleQuote(s string) string {
	return "'" + strings.ReplaceAll(s, "'", `'\''`) + "'"
}

