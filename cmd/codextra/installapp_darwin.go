// writeDesktopAppIcon renders the codextra logo into a .icns using macOS's own
// iconutil over a generated .iconset of the standard sizes.
func writeDesktopAppIcon(icnsPath string) error {
	tmp, err := os.MkdirTemp("", "codextra-iconset-")
	if err != nil {
		return err
	}
	defer os.RemoveAll(tmp)

	iconset := filepath.Join(tmp, "codextra.iconset")
	if err := os.MkdirAll(iconset, 0o755); err != nil {
		return err
	}

	// Render one anti-aliased 1024 master, then box-downsample it (by integer
	// factors, since 1024 divides each target) to every iconset size — far
	// cheaper than re-rendering each size at high supersample.
	master, err := renderAppIcon(1024)
	if err != nil {
		return err
	}
	for _, e := range []struct {
		name string
		px   int
	}{
		{"icon_16x16.png", 16}, {"icon_16x16@2x.png", 32},
		{"icon_32x32.png", 32}, {"icon_32x32@2x.png", 64},
		{"icon_128x128.png", 128}, {"icon_128x128@2x.png", 256},
		{"icon_256x256.png", 256}, {"icon_256x256@2x.png", 512},
		{"icon_512x512.png", 512}, {"icon_512x512@2x.png", 1024},
	} {
		img := master
		if e.px != 1024 {
			img = downsample(master, 1024/e.px)
		}
		var buf bytes.Buffer
		if err := png.Encode(&buf, img); err != nil {
			return err
		}
		if err := os.WriteFile(filepath.Join(iconset, e.name), buf.Bytes(), 0o644); err != nil {
			return err
		}
	}

	if out, err := exec.Command("iconutil", "-c", "icns", iconset, "-o", icnsPath).CombinedOutput(); err != nil {
		return fmt.Errorf("iconutil: %w: %s", err, strings.TrimSpace(string(out)))
	}
	return nil
}

// appIconPNG renders the app icon at the given size and returns it as PNG bytes.
func appIconPNG(size int) ([]byte, error) {
	img, err := renderAppIcon(size)
	if err != nil {
		return nil, err
	}
	var buf bytes.Buffer
	if err := png.Encode(&buf, img); err != nil {
		return nil, err
	}
	return buf.Bytes(), nil
}

// renderAppIcon draws the logo (black) centered on a white rounded-square tile at
// the given size, supersampled 2x for smooth edges.
func renderAppIcon(size int) (*image.RGBA, error) {
	logoPaths, err := loadLogoSVGPaths()
	if err != nil {
		return nil, err
	}

	const ss = 2
	big := size * ss
	canvas := image.NewRGBA(image.Rect(0, 0, big, big))

	fillRoundedRect(canvas, big, float64(big)*0.22, color.RGBA{R: 0xff, G: 0xff, B: 0xff, A: 0xff})

	const viewBox = 20.0
	const logoFrac = 0.6
	scale := float64(big) * logoFrac / viewBox
	offset := (float64(big) - viewBox*scale) / 2

	var contours []pathContour
	for idx, raw := range logoPaths {
		pc, perr := parsePathContours(raw, func(x, y float64) point {
			return point{x: offset + x*scale, y: offset + y*scale}
		})
		if perr != nil {
			return nil, fmt.Errorf("svg path %d: %w", idx, perr)
		}
		contours = append(contours, pc...)
	}
	fillContours(canvas, contours, color.RGBA{A: 0xff})

	return downsample(canvas, ss), nil
}

// fillRoundedRect fills a rounded square of the given side and corner radius.
func fillRoundedRect(img *image.RGBA, side int, radius float64, c color.RGBA) {
	max := float64(side)
	for y := 0; y < side; y++ {
		for x := 0; x < side; x++ {
			fx, fy := float64(x)+0.5, float64(y)+0.5
			var dx, dy float64
			switch {
			case fx < radius:
				dx = radius - fx
			case fx > max-radius:
				dx = fx - (max - radius)
			}
			switch {
			case fy < radius:
				dy = radius - fy
			case fy > max-radius:
				dy = fy - (max - radius)
			}
			if dx*dx+dy*dy <= radius*radius {
				img.SetRGBA(x, y, c)
			}
		}
	}
}

// downsample box-filters img by factor, premultiplied-correct so the transparent
// rounded corners don't leave a gray fringe.
func downsample(src *image.RGBA, factor int) *image.RGBA {
	w := src.Bounds().Dx() / factor
	h := src.Bounds().Dy() / factor
	dst := image.NewRGBA(image.Rect(0, 0, w, h))
	n := factor * factor
	for y := 0; y < h; y++ {
		for x := 0; x < w; x++ {
			var pr, pg, pb, pa int
			for dy := 0; dy < factor; dy++ {
				for dx := 0; dx < factor; dx++ {
					c := src.RGBAAt(x*factor+dx, y*factor+dy)
					a := int(c.A)
					pr += int(c.R) * a / 255
					pg += int(c.G) * a / 255
					pb += int(c.B) * a / 255
					pa += a
				}
			}
			avgA := pa / n
			var rr, gg, bb int
			if avgA > 0 {
				rr = clamp255(pr / n * 255 / avgA)
				gg = clamp255(pg / n * 255 / avgA)
				bb = clamp255(pb / n * 255 / avgA)
			}
			dst.SetRGBA(x, y, color.RGBA{R: uint8(rr), G: uint8(gg), B: uint8(bb), A: uint8(avgA)})
		}
	}
	return dst
}

func clamp255(v int) int {
	switch {
	case v > 255:
		return 255
	case v < 0:
		return 0
	default:
		return v
	}
}
